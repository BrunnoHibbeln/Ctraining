#include <unistd.h>

int	main(int c, char *v[])
{
	int i;

	i = 0;
	if (c < 2)
	{
		write(1, "a", 1);
		write(1, "\n", 1);
		return (0);
	}
	while (v[1][i])
	{
		if (v[1][i] == 'a')
		{
			write(1, "a", 1);
			write(1, "\n", 1);
			return (0);
		}
		i++;
	}
}